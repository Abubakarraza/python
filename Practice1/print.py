#print accept 4 parameter 
# 1 : which you want to print string object array or you can print multiple
print("Hello World",4*3)
# 2 : sep : which is used to separate multiple print by default it is space
print("Hello World",8,sep="~")
# 3 : end : Which is used where our print is end by default it is new line
print("Hello World",99,sep="%",end="This is end of print")